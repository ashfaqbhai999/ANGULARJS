// number example
var age = 25;
console.log('value : ' + age + ' data type : ' + typeof age);

// String Examples
var course = 'JavaScript';
console.log('value : ' + course + ' data type : ' + typeof course);

// boolean Examples
var isJSEasy = true;
console.log('value : ' + isJSEasy + ' data type : ' + typeof isJSEasy);

// reassignment example of variables
var abc;
console.log('value : ' + abc + ' data type : ' + typeof abc);

abc = 10;
console.log('value : ' + abc + ' data type : ' + typeof abc);

abc = 'test';
console.log('value : ' + abc + ' data type : ' + typeof abc);

abc = true;
console.log('value : ' + abc + ' data type : ' + typeof abc);

abc = null;
console.log('value : ' + abc + ' data type : ' + typeof abc);
